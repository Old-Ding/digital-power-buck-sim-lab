    'Solver', 'ode4', ...
    'FixedStep', '2.5e-7', ...
    'StopTime', '0.012', ...
    'SimulationMode', 'normal');

add_block('simulink/Sources/Constant', [modelName '/Vref 12V'], ...
    'Position', [60 115 130 145], ...
    'Value', '12');

add_block('simulink/Sources/Step', [modelName '/Vin step 24V to 20V'], ...
    'Position', [520 255 675 295], ...
    'Time', '0.003', ...
    'Before', '24', ...
    'After', '20');

add_block('simulink/Sources/Step', [modelName '/Rload step 2.4ohm to 1.6ohm'], ...
    'Position', [480 365 675 405], ...
    'Time', '0.007', ...
    'Before', '2.4', ...
    'After', '1.6');

add_block('simulink/Ports & Subsystems/Subsystem', [modelName '/Discrete PI voltage controller'], ...
    'Position', [300 80 540 220]);
build_controller_subsystem([modelName '/Discrete PI voltage controller']);

add_block('simulink/Ports & Subsystems/Subsystem', [modelName '/Averaged Buck plant'], ...
    'Position', [735 200 985 415]);
build_plant_subsystem([modelName '/Averaged Buck plant']);

add_block('simulink/Signal Routing/Mux', [modelName '/Scope mux'], ...
    'Position', [1320 120 1350 320], ...
    'Inputs', '6');

add_block('simulink/Sinks/Scope', [modelName '/Scope Vout duty error'], ...
    'Position', [1415 190 1515 280], ...
    'NumInputPorts', '1');

add_block('simulink/Signal Routing/From', [modelName '/from Vout feedback'], ...
    'Position', [190 165 260 195], ...
    'GotoTag', 'Vout_fb');

add_block('simulink/Signal Routing/Goto', [modelName '/goto Vout feedback'], ...
    'Position', [1030 210 1110 240], ...
    'GotoTag', 'Vout_fb');

add_block('simulink/Signal Routing/Goto', [modelName '/goto duty monitor'], ...
    'Position', [600 95 665 125], ...
    'GotoTag', 'duty_cmd');

add_block('simulink/Signal Routing/Goto', [modelName '/goto error monitor'], ...
    'Position', [600 145 665 175], ...
    'GotoTag', 'pi_error');

add_block('simulink/Signal Routing/Goto', [modelName '/goto integrator monitor'], ...
    'Position', [600 195 665 225], ...
    'GotoTag', 'pi_xI');

add_block('simulink/Signal Routing/Goto', [modelName '/goto IL monitor'], ...
    'Position', [1030 275 1110 305], ...
    'GotoTag', 'IL_avg');

add_block('simulink/Signal Routing/Goto', [modelName '/goto Iout monitor'], ...
    'Position', [1030 355 1110 385], ...
    'GotoTag', 'Iout_avg');

add_monitor_from(modelName, 'mon Vout', 'Vout_fb', [1210 115 1285 145], 1);
add_monitor_from(modelName, 'mon duty', 'duty_cmd', [1210 150 1285 180], 2);
add_monitor_from(modelName, 'mon error', 'pi_error', [1210 185 1285 215], 3);
add_monitor_from(modelName, 'mon xI', 'pi_xI', [1210 220 1285 250], 4);
add_monitor_from(modelName, 'mon IL', 'IL_avg', [1210 255 1285 285], 5);
add_monitor_from(modelName, 'mon Iout', 'Iout_avg', [1210 290 1285 320], 6);

add_line(modelName, 'Vref 12V/1', 'Discrete PI voltage controller/1', 'autorouting', 'on');
add_line(modelName, 'from Vout feedback/1', 'Discrete PI voltage controller/2', 'autorouting', 'on');
add_line(modelName, 'Discrete PI voltage controller/1', 'Averaged Buck plant/2', 'autorouting', 'on');
add_line(modelName, 'Vin step 24V to 20V/1', 'Averaged Buck plant/1', 'autorouting', 'on');
add_line(modelName, 'Rload step 2.4ohm to 1.6ohm/1', 'Averaged Buck plant/3', 'autorouting', 'on');

add_line(modelName, 'Discrete PI voltage controller/1', 'goto duty monitor/1', 'autorouting', 'on');
add_line(modelName, 'Discrete PI voltage controller/2', 'goto error monitor/1', 'autorouting', 'on');
add_line(modelName, 'Discrete PI voltage controller/3', 'goto integrator monitor/1', 'autorouting', 'on');
add_line(modelName, 'Averaged Buck plant/1', 'goto Vout feedback/1', 'autorouting', 'on');
add_line(modelName, 'Averaged Buck plant/2', 'goto IL monitor/1', 'autorouting', 'on');
add_line(modelName, 'Averaged Buck plant/3', 'goto Iout monitor/1', 'autorouting', 'on');
add_line(modelName, 'Scope mux/1', 'Scope Vout duty error/1', 'autorouting', 'on');
