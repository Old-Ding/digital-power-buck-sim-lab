function add_monitor_from(modelName, blockName, tagName, position, muxPort)
    add_block('simulink/Signal Routing/From', [modelName '/' blockName], ...
        'Position', position, ...
        'GotoTag', tagName);
    add_line(modelName, [blockName '/1'], ['Scope mux/' num2str(muxPort)], 'autorouting', 'on');
end

function build_controller_subsystem(sys)
    open_system(sys);
    delete_line(sys, 'In1/1', 'Out1/1');
    delete_block([sys '/In1']);
    delete_block([sys '/Out1']);

    add_block('simulink/Sources/In1', [sys '/Vref'], ...
        'Position', [45 75 75 95]);
    add_block('simulink/Sources/In1', [sys '/Vout'], ...
        'Position', [45 150 75 170]);
    add_block('simulink/Math Operations/Sum', [sys '/e = Vref - Vout'], ...
        'Position', [125 95 160 135], ...
        'Inputs', '+-');
    add_block('simulink/Commonly Used Blocks/Gain', [sys '/Kp = 0.05'], ...
        'Position', [215 60 305 100], ...
        'Gain', '0.05');
    add_block('simulink/Discrete/Discrete-Time Integrator', [sys '/Ki accumulator'], ...
        'Position', [215 130 315 180], ...
        'gainval', '200', ...
        'SampleTime', '5e-6', ...
        'InitialCondition', '0.0041667');
    add_block('simulink/Sources/Constant', [sys '/Dff = 0.5'], ...
        'Position', [215 225 295 255], ...
        'Value', '0.5');
    add_block('simulink/Math Operations/Sum', [sys '/duty = Dff + P + I'], ...
        'Position', [380 115 420 175], ...
        'Inputs', '+++');
    add_block('simulink/Discrete/Zero-Order Hold', [sys '/PWM update Ts=5us'], ...
        'Position', [475 125 595 165], ...
        'SampleTime', '5e-6');
    add_block('simulink/Sinks/Out1', [sys '/duty'], ...
        'Position', [660 130 690 150]);
    add_block('simulink/Sinks/Out1', [sys '/error'], ...
        'Position', [660 205 690 225]);
    add_block('simulink/Sinks/Out1', [sys '/xI'], ...
        'Position', [660 265 690 285]);

    add_line(sys, 'Vref/1', 'e = Vref - Vout/1', 'autorouting', 'on');
    add_line(sys, 'Vout/1', 'e = Vref - Vout/2', 'autorouting', 'on');
    add_line(sys, 'e = Vref - Vout/1', 'Kp = 0.05/1', 'autorouting', 'on');
    add_line(sys, 'e = Vref - Vout/1', 'Ki accumulator/1', 'autorouting', 'on');
    add_line(sys, 'Kp = 0.05/1', 'duty = Dff + P + I/1', 'autorouting', 'on');
    add_line(sys, 'Ki accumulator/1', 'duty = Dff + P + I/2', 'autorouting', 'on');
    add_line(sys, 'Dff = 0.5/1', 'duty = Dff + P + I/3', 'autorouting', 'on');
    add_line(sys, 'duty = Dff + P + I/1', 'PWM update Ts=5us/1', 'autorouting', 'on');
    add_line(sys, 'PWM update Ts=5us/1', 'duty/1', 'autorouting', 'on');
    add_line(sys, 'e = Vref - Vout/1', 'error/1', 'autorouting', 'on');
    add_line(sys, 'Ki accumulator/1', 'xI/1', 'autorouting', 'on');

    Simulink.BlockDiagram.arrangeSystem(sys);
    close_system(sys);
end

function build_plant_subsystem(sys)
    open_system(sys);
    delete_line(sys, 'In1/1', 'Out1/1');
    delete_block([sys '/In1']);
    delete_block([sys '/Out1']);

    add_block('simulink/Sources/In1', [sys '/Vin'], ...
        'Position', [45 65 75 85]);
    add_block('simulink/Sources/In1', [sys '/duty'], ...
        'Position', [45 125 75 145]);
    add_block('simulink/Sources/In1', [sys '/Rload'], ...
        'Position', [45 330 75 350]);

    add_block('simulink/Math Operations/Product', [sys '/Vin * duty'], ...
        'Position', [135 85 175 130], ...
        'Inputs', '**');
    add_block('simulink/Math Operations/Sum', [sys '/vL = VinD - Vout - iLr'], ...
        'Position', [270 115 310 175], ...
        'Inputs', '+--');
    add_block('simulink/Commonly Used Blocks/Gain', [sys '/1 over L 22uH'], ...
        'Position', [360 125 455 165], ...
        'Gain', '1/22e-6');
    add_block('simulink/Continuous/Integrator', [sys '/iL'], ...
        'Position', [505 125 555 165], ...
        'InitialCondition', '5');
    add_block('simulink/Commonly Used Blocks/Gain', [sys '/Rseries 0.02ohm'], ...
        'Position', [610 190 710 225], ...
        'Gain', '0.02');

    add_block('simulink/Math Operations/Product', [sys '/Iout = Vout div Rload'], ...
        'Position', [505 300 550 345], ...
        'Inputs', '*/');
    add_block('simulink/Math Operations/Sum', [sys '/iC = iL - Iout'], ...
        'Position', [610 270 650 320], ...
        'Inputs', '+-');
    add_block('simulink/Commonly Used Blocks/Gain', [sys '/1 over C 100uF'], ...
        'Position', [700 278 805 312], ...
        'Gain', '1/100e-6');
    add_block('simulink/Continuous/Integrator', [sys '/Vout'], ...
        'Position', [850 275 900 315], ...
        'InitialCondition', '12');

    add_block('simulink/Sinks/Out1', [sys '/Vout out'], ...
        'Position', [995 285 1025 305]);
    add_block('simulink/Sinks/Out1', [sys '/IL out'], ...
        'Position', [995 135 1025 155]);
    add_block('simulink/Sinks/Out1', [sys '/Iout out'], ...
        'Position', [995 360 1025 380]);

    add_line(sys, 'Vin/1', 'Vin * duty/1', 'autorouting', 'on');
    add_line(sys, 'duty/1', 'Vin * duty/2', 'autorouting', 'on');
    add_line(sys, 'Vin * duty/1', 'vL = VinD - Vout - iLr/1', 'autorouting', 'on');
    add_line(sys, 'vL = VinD - Vout - iLr/1', '1 over L 22uH/1', 'autorouting', 'on');
    add_line(sys, '1 over L 22uH/1', 'iL/1', 'autorouting', 'on');
    add_line(sys, 'iL/1', 'Rseries 0.02ohm/1', 'autorouting', 'on');
    add_line(sys, 'Rseries 0.02ohm/1', 'vL = VinD - Vout - iLr/3', 'autorouting', 'on');
    add_line(sys, 'iL/1', 'iC = iL - Iout/1', 'autorouting', 'on');
    add_line(sys, 'iL/1', 'IL out/1', 'autorouting', 'on');

    add_line(sys, 'Vout/1', 'vL = VinD - Vout - iLr/2', 'autorouting', 'on');
    add_line(sys, 'Vout/1', 'Iout = Vout div Rload/1', 'autorouting', 'on');
    add_line(sys, 'Rload/1', 'Iout = Vout div Rload/2', 'autorouting', 'on');
    add_line(sys, 'Iout = Vout div Rload/1', 'iC = iL - Iout/2', 'autorouting', 'on');
    add_line(sys, 'Iout = Vout div Rload/1', 'Iout out/1', 'autorouting', 'on');
    add_line(sys, 'iC = iL - Iout/1', '1 over C 100uF/1', 'autorouting', 'on');
    add_line(sys, '1 over C 100uF/1', 'Vout/1', 'autorouting', 'on');
    add_line(sys, 'Vout/1', 'Vout out/1', 'autorouting', 'on');

    Simulink.BlockDiagram.arrangeSystem(sys);
    close_system(sys);
end
